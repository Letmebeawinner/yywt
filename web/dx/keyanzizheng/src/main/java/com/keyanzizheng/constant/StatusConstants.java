package com.keyanzizheng.constant;

/**
 * 状态的常量类
 * 1表示是
 * 0表是否
 * <br>
 * 例  1: 删除 0 : 未删除
 *
 * @author YaoZhen
 * @date 12-21, 15:21, 2017.
 */
public class StatusConstants {

    /**
     * 否
     * 用在status字段上时表示未删除
     */
    public static final int NEGATE = 0;

    /**
     * 是
     * 用在status字段上时表示已删除
     */
    public static final int DONE = 1;

}
